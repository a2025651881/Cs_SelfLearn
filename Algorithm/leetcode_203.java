public class leetcode_203 {

    public class ListNode {
        int val;
        ListNode next;

        ListNode() {
        }

        ListNode(int val) {
            this.val = val;
        }

        ListNode(int val, ListNode next) {
            this.val = val;
            this.next = next;
        }
    }

    public ListNode removeElements(ListNode head, int val) {
        // sentinel节点保持一致性
        ListNode sentienalNode = new ListNode(0, head);
        ListNode point = sentienalNode;
        while (point.next != null) {
            if (point.next.val == val) {
                point.next = point.next.next;
            } else {
                point = point.next;
            }
        }
        return sentienalNode.next;
    }
}
